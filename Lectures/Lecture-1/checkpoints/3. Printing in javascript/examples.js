// Example 1: Printing a simple message
// This will print the string "Hello, world!" to the console.
console.log("Hello, world!");  // Output: Hello, world!

// Example 2: Printing a number
// This will print the number 25 to the console.
console.log(25);  // Output: 25

// Example 3: Printing multiple values
// You can print several values at once by separating them with commas.
console.log("Hello", "this is", "JavaScript!");  // Output: Hello this is JavaScript!

// Example 4: Printing a new line using an empty console.log()
// To print a new line, use an empty console.log().
console.log("This is the first line.");
console.log("");  // This prints a blank line (new line)
console.log("This is the second line.");

// Example 5: Printing a new line using \n (newline character)
// You can also print a new line by including the \n character in a string.
console.log("This is the first line.\nThis is the second line.");
// Output: 
// This is the first line.
// This is the second line.

// Example 6: Printing math calculations
// You can print the result of math calculations directly.
console.log(5 + 10);  // Output: 15
console.log(100 - 25); // Output: 75

// Example 7: Printing a combination of text and numbers
// You can print a combination of text and the result of a calculation.
console.log("The answer is:", 5 + 10);  // Output: The answer is: 15

// Example 8: Printing the result of a condition
// You can print the result of a condition (true or false) based on a comparison.
console.log(10 > 5);  // Output: true
console.log(5 > 10);  // Output: false
